/**
 * Tape file generator — converts a `ScreenshotRecipe` into a VHS tape
 * file (the DSL VHS uses to drive a PTY: keystrokes, sleeps, capture
 * directives, etc.).
 *
 * Each recipe produces a single `.tape` file under `.screenshots/`
 * which we then hand to the `vhs` CLI. Keeping the tape generation
 * pure (no I/O) makes it easy to test and inspect.
 */

import type { ScreenshotRecipe, ScreenshotRecipeAction } from './recipes'
import { SNAPSHOT_NOW } from './recipes'

type TapeOptions = {
  /**
   * Absolute path to the temporary git scenario directory the recipe
   * spun up. The tape `cd`s here before launching coco.
   */
  cwd: string
  /**
   * Output PNG path (the recipe's name with `.png` appended).
   */
  outputPng: string
  /**
   * Optional output GIF path. Emitted when `recipe.emitGif` is true.
   */
  outputGif?: string
  /**
   * Path to the coco CLI entry point we should run. Typically
   * `<repo>/node_modules/.bin/tsx <repo>/src/index.ts`.
   */
  cocoCommand: string
  /**
   * Absolute path to the repo root. Used to prepend
   * `node_modules/.bin` to PATH so the VHS shell can find `tsx`,
   * `node`, etc. without relying on the user's global PATH.
   */
  repoRoot: string
  /**
   * Directory containing the `node` binary (dirname of
   * `process.execPath`). Prepended to PATH so tsx can find node
   * at runtime inside the VHS shell.
   */
  nodeBinDir: string
}

const DEFAULT_DIMENSIONS = { cols: 140, rows: 40 } as const

/**
 * How long to wait after the command launches before capturing. The
 * workstation's three-stage boot (cache read → mount → background
 * fetch) needs time for tsx cold-start (~2-3s inside VHS) plus the
 * async git data load (~1-2s). 5000ms gives comfortable headroom.
 */
const POST_LAUNCH_SETTLE_MS = 5000

/**
 * Quote a string for safe inclusion in a VHS `Type "..."` directive.
 * VHS uses double-quoted strings; backslashes and double-quotes need
 * escaping. Newlines aren't supported inside Type — callers should
 * use `Enter` for those.
 */
function quoteTapeString(text: string): string {
  return text.replace(/\\/g, '\\\\').replace(/"/g, '\\"')
}

function renderAction(action: ScreenshotRecipeAction): string[] {
  switch (action.kind) {
    case 'sleep':
      return [`Sleep ${action.ms}ms`]
    case 'type':
      return [`Type "${quoteTapeString(action.text)}"`]
    case 'key': {
      const count = action.count && action.count > 1 ? ` ${action.count}` : ''
      return [`${action.key}${count}`]
    }
  }
}

/**
 * Keys to forward from the driver's process.env into the VHS shell.
 * These enable AI-powered commands (commit, changelog, review) to
 * make real LLM calls for demo GIFs. Only exported if present.
 */
const FORWARDED_ENV_KEYS = [
  // AI providers
  'OPENAI_API_KEY',
  'ANTHROPIC_API_KEY',
  'OLLAMA_HOST',
  'OPENROUTER_API_KEY',
  'COCO_SERVICE_PROVIDER',
  'COCO_SERVICE_MODEL',
  'COCO_SERVICE_BASE_URL',
  'COCO_SERVICE_ENDPOINT',
  // GitHub CLI — needed for PR counts, issue triage, etc.
  'GH_TOKEN',
  'GITHUB_TOKEN',
]

function buildEnvExports(): string[] {
  const lines: string[] = []
  for (const key of FORWARDED_ENV_KEYS) {
    const value = process.env[key]
    if (value) {
      // Use unquoted export — values shouldn't contain spaces.
      // If they do, the user should set them in their shell directly.
      lines.push(`Type "export ${key}=${value}"`)
      lines.push(`Enter`)
      lines.push(`Sleep 100ms`)
    }
  }
  return lines
}

/**
 * Build the VHS tape content for a recipe. The result is a string
 * ready to write to a `.tape` file.
 */
export function buildTape(recipe: ScreenshotRecipe, options: TapeOptions): string {
  const dims = recipe.dimensions || DEFAULT_DIMENSIONS

  const header = [
    `# Auto-generated by bin/screenshot.ts — do not edit by hand.`,
    `# Recipe: ${recipe.name}`,
    `# ${recipe.description}`,
    ``,
    // No `Output` directive for screenshots — `Output` records the
    // entire session as a frame sequence or GIF. We use `Screenshot`
    // at the end to capture a single PNG at the settled frame.
    // GIF output (when emitGif is true) still uses Output.
    ...(options.outputGif ? [`Output "${options.outputGif}"`] : []),
    ``,
    `Set Shell "bash"`,
    `Set FontSize 14`,
    `Set Width ${dims.cols * 9}`,
    `Set Height ${dims.rows * 20}`,
    `Set TypingSpeed 50ms`,
    `Set CursorBlink false`,
    // Pin the colour theme of the rendered terminal itself so VHS's
    // xterm.js palette doesn't drift if the upstream default changes.
    // (This is the *terminal's* theme, separate from coco's --theme.)
    `Set Theme "Catppuccin Mocha"`,
    ``,
    // Hide the boot of the command (cd + the launch line) so the
    // captured frame is just the rendered view, not "$ coco ui …".
    `Hide`,
    // Type setup commands instantly (0ms) so the shell processes
    // them before the visible portion starts. The default TypingSpeed
    // only applies to the visible command for aesthetics.
    `Set TypingSpeed 0ms`,
    `Type "cd ${quoteTapeString(options.cwd)}"`,
    `Enter`,
    `Sleep 300ms`,
    // VHS spawns a fresh bash without the user's gitconfig. Mark all
    // directories as safe so git doesn't refuse to operate on the
    // temp scenario dir (macOS safe.directory check).
    `Type "git config --global --add safe.directory '*'"`,
    `Enter`,
    `Sleep 300ms`,
    // Ensure the VHS shell can find node + tsx + git + gh + system utils.
    // Include /usr/local/bin and /opt/homebrew/bin for tools like gh.
    // $PATH expands to the shell's existing PATH (includes /usr/bin etc).
    `Type "export PATH=${quoteTapeString(options.repoRoot)}/node_modules/.bin:${quoteTapeString(options.nodeBinDir)}:/usr/local/bin:/opt/homebrew/bin:$PATH"`,
    `Enter`,
    `Sleep 300ms`,
    // Snapshot mode pin — freezes wall-clock `now` for relative
    // date formatters in the workstation render path.
    `Type "export COCO_SNAPSHOT_NOW='${SNAPSHOT_NOW}'"`,
    `Enter`,
    `Sleep 300ms`,
    // Forward API keys from the driver's env (loaded from .env) into
    // the VHS shell so AI-powered commands (commit, changelog, review)
    // can make real LLM calls for demo GIFs.
    ...buildEnvExports(),
    // NO_COLOR is set per-recipe via theme=monochrome; otherwise we
    // let coco's own theme machinery paint.
    recipe.theme === 'monochrome' ? `Type "export NO_COLOR=1"` : null,
    recipe.theme === 'monochrome' ? `Enter` : null,
    `Type "clear"`,
    `Enter`,
    `Sleep 200ms`,
    // For GIF recipes: keep hidden through boot so the recording
    // starts with the fully-loaded UI, not the typing/loading phase.
    // For screenshot-only recipes: Show before the command.
    ...(recipe.emitGif ? [] : [`Show`, ``]),
    // Launch coco with --repo pointing at the scenario dir. Keep
    // TypingSpeed at 0 so the command executes immediately.
    `Type "${quoteTapeString(options.cocoCommand)} ${quoteTapeString(recipe.command)} --repo ${quoteTapeString(options.cwd)}"`,
    `Enter`,
    `Sleep ${POST_LAUNCH_SETTLE_MS}ms`,
    // For GIF recipes: Show AFTER the settle — recording starts
    // with the fully-rendered UI, no boot frames visible.
    ...(recipe.emitGif ? [`Show`, ``] : []),
  ]

  const actionLines = (recipe.actions || []).flatMap((action) => renderAction(action))

  // Final settle before VHS captures the closing frame — gives any
  // late-arriving render (e.g. async scenario context loaders) time
  // to land before the screenshot is taken.
  const trailer = [
    ``,
    `Sleep 500ms`,
    // Take a single screenshot. VHS resolves the path relative to
    // its own cwd (which the driver sets to the scenario dir). Use
    // a bare filename; the driver moves it to the final location.
    `Screenshot screenshot.png`,
    `Sleep 200ms`,
    // Quit the workstation cleanly so VHS doesn't hold the PTY open.
    // For non-TTY commands (`coco log`) this is a no-op since the
    // command already exited.
    `Type "q"`,
    `Sleep 200ms`,
  ]

  return [
    ...header.filter((line): line is string => line !== null),
    ...actionLines,
    ...trailer,
  ].join('\n') + '\n'
}
